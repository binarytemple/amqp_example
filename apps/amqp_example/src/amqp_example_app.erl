-module(amqp_example_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    Env = application:get_all_env(amqp_example),
    lager:log(info,self(),"START ENV = ~p ~n", [Env]),
    amqp_example_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

